#include <stdlib.h>
#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = c;
	while (*s)
	{
		if (*s == ch)
			return ((char *)s);
		++s;
	}
	if (*s == ch)
		return ((char *)s);
	return (NULL);
}
